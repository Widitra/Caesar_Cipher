def caesar(plain_text, shift_amount, directional_encryption):
    end_letter = ""
    for every_letter in plain_text:
        if every_letter in alphabet:
            initial_position = alphabet.index(every_letter)
            if directional_encryption == "encode":
                new_position = initial_position + shift_amount
            else:
                new_position = initial_position - shift_amount
            new_letter = alphabet[new_position]
            end_letter += new_letter
        else:
            end_letter += every_letter
    print(f"The new word is {end_letter}")
    ask_again = input("Do you want to continue? (Type 'yes' to continue, type 'no' to end program)\n").lower()
    if ask_again == "no":
        print("Goodbye")
        quit()
    else:
        print("")


